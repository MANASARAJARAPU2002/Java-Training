package JAVAFINAL.REVIEW.PREPARARTION;

public class ShiftOperator_Demo {

	public static void main(String[] args) {
		int a=-10;
		int b=a<<2;
		int c=a>>2;
		int d=a>>>2;
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(~a); 
		
	}

}
